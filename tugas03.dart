void main() {
  for (int i = 1; i <= 100; i++) {
    if (i % 2 == 0) {
      print("$i adalah bilangan genap.");
    } else {
      print("$i adalah bilangan ganjil.");
    }
  }
  int j = 1;
  while (j <= 5) {
    if (j % 2 == 0) {
      print("$j adalah bilangan genap.");
    } else {
      print("$j adalah bilangan ganjil.");
    }
    j++;
  }
}